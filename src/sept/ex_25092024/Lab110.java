package sept.ex_25092024;

public class Lab110 {
    public static void main(String[] args) {
        // while
        // while - a, b, c
        //    //a - init
        //    //b - condition
        //    //c - increment/decrement

        int i = 0;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        for (int j = 0; j <=10; j++) {
            System.out.println(j);
        }
    }
}
